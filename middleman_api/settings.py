# Firebase Configuration
FIREBASE_CREDENTIALS_PATH = os.getenv('FIREBASE_CREDENTIALS_PATH')

if FIREBASE_CREDENTIALS_PATH:
    if not firebase_admin._apps:
        try:
            cred = None
            # Check if it's a URL (e.g. Cloudflare R2)
            if FIREBASE_CREDENTIALS_PATH.startswith('http://') or FIREBASE_CREDENTIALS_PATH.startswith('https://'):
                try:
                    response = requests.get(FIREBASE_CREDENTIALS_PATH, timeout=10)
                    response.raise_for_status()
                    cred_dict = response.json()
                    cred = credentials.Certificate(cred_dict)
                except requests.RequestException as e:
                    print(f"Warning: Failed to fetch Firebase credentials from URL: {e}")
            # Check if it's a local file path
            elif os.path.exists(FIREBASE_CREDENTIALS_PATH):
                cred = credentials.Certificate(FIREBASE_CREDENTIALS_PATH)
            else:
                 print(f"Warning: FIREBASE_CREDENTIALS_PATH is set but file not found locally: {FIREBASE_CREDENTIALS_PATH}")

            if cred:
                firebase_admin.initialize_app(cred)
                
        except Exception as e:
            print(f"Warning: Failed to initialize Firebase Admin: {e}")
else:
    # Fallback or warning
    # For development without credentials, we might skip this or initialize default
    # But for now, we assume credentials are provided or handled.
    print("Warning: FIREBASE_CREDENTIALS_PATH not set. Firebase Admin not initialized.")

